<p><em><strong>Pegasus, the Winged Horse – this month’s sky
highlight</strong></em></p>

<p></p>

<p>Pegasus contains the large recognizable pattern of stars called the Great
Square of Pegasus. Pegasus was the winged horse of mythology, created by
Neptune, born of sand, sea-foam and Medusa’s blood.</p>

<p>For ease, we start this observation at 2330. This is when the Great Square
of Pegasus is exactly due South. It’s stars are not the brightest, but
because they create a square-ish pattern 15 degrees wide and high, they’re
recognisable. To find the Square, look approximately 60 degrees in elevation.
If you’re struggling, but you can see Cassiopeia, use this as a guide. Set
yourself traveling in a five o’clock direction for 30 degrees. This will
bring you to the Square.</p>

<p>To the right of the Square is Andromeda. We’re now going to find a galaxy,
which is 2.9 million light years from us. If you can see it with the unaided
eye you are gazing upon one of the most distant objects visible without optical
aid.</p>

<p>Take the uppermost left-hand star of the Square and travel for six degrees
in a 10 o’clock direction and you will arrive at a bright-ish star. Move from
here in an 11 o’clock direction for seven degrees and you will see a brighter
star called Mirach. Stop here. Now, three degrees away in a 10 o’clock
direction you will find another faint-ish star. Aim a pair of binoculars at
this position. Slowly continue to move in the same direction for another three
degrees. As you do you will sweep-up a fuzzy patch. This is the great Andromeda
Galaxy – M31.</p>

<p>This is one of the most magnificent objects in the night sky and undoubtedly
the most famous galaxy outside our own Milky Way. Its extent covers as much of
the sky as five full moons put together. Binoculars will show M31 with a clear
brightening towards the centre. They may also show one of two of Andromeda’s
companion galaxies – M32. If you are lucky you may also see M110. Careful
observation of the nuclear region with a telescope will reveal faint dust lanes
and other structures. The Hubble Space Telescope has shown that the Andromeda
Galaxy has a double nucleus, indicating that it may have cannibalised another
galaxy. M31 was once thought to be a nebula inside our galaxy, but in 1923,
astronomer Edwin Hubble showed that it lies outside the Milky Way. The galaxy
is over 150000 light years across, and has a mass 1.2 trillion times that of
our Sun.</p>

<p></p>

<p><em><strong>In history …</strong></em></p>

<p>Johann Galle of the Berlin Observatory saw and identified Neptune on the
night of September 23rd 1846.</p>

<p>On September 1st 1859 Richard Carrington observed and recorded the first
solar flare. The next night a beautiful aurora was witnessed by millions of
people at northern latitudes.</p>

<p>Nearly a century later, on September 12th 1959, the USSR crash-landed Luna 2
onto the Moon, making it the first manmade object to strike another world.</p>

<p>On September 24 1970 the USSR’s unmanned vehicle, Luna 16, returned to
Earth carrying three ounces of samples from the Moon. (Meanwhile, the crews of
Apollo 11 and 12 had returned over 140 pounds of lunar materials.)</p>

<p>Viking 2 landed safely on Mars on the 3rd of September 1976. (Viking 1 had
landed on Mars on July 20th, 1976.)</p>

