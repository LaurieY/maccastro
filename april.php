<h4 style="text-align:center;margin-left:auto;margin-right:auto;">The night sky
in April</h4>

<p><em>Want to know what's up in the sky tonight? Our night sky chart will
provide you with the information you need to locate the brightest planets,
stars and deep-sky objects ...</em></p>

<p>During April the Lyrid meteor shower begins around the 19th and reaches its
peak on the 22nd and is over by April 25. This is not a particularly good
shower, averaging only 10 meteors per hour, but anything can happen. If you
have nothing better to do at around 3 o’ clock in the morning during those
nights (!), step outside and watch the skies. </p>

<p>At that time the constellation of Lyra, the ‘source’ of the Lyrid
shower, should be right overhead. The parent body of this shower is Comet
Thatcher …</p>

