<p><em><strong>In history …</strong></em></p>

<p>On the 2nd of April 1845 the first photograph of the Sun was taken (with
special filters).</p>

<p>Frank Drake began Project Ozma, the first search for extraterrestrial radio
transmissions, on April 11th 1960. He found nothing.</p>

<p>One year and one day later (April 12, 1961) Yuri Gagarin became the first
Earthling in space. He completed one orbit of our world in his spaceship Vostok
1.</p>

<p>Twenty years later (April 12, 1981) the first Space Shuttle, Columbia,
carried Robert L. Crippen and John W. Young into space. Two days later they
reentered the Earth’s atmosphere and glided to a landing, like a plane,
proving that the Shuttle could be reused for further flights.</p>

<p>The Hubble Space Telescope was deployed on April 25 1990. Four weeks later
the first images from the scope showed there was something seriously wrong with
the optics. It was later determined that years before, during production, a
misplaced fleck of paint had introduced an error into the curvature of the 2.4
meter mirror – making it too shallow by 0.002 millimeters! This caused
‘spherical aberration’ and it was impossible to focus the ‘scope. Some
projects could continue but Hubble was unable to carry out its complete
mission. In December 1993 the crew of the Space Shuttle Endeavour captured the
telescope and repaired it by introducing an extra optical aid (that acts like
corrective glasses).</p>

