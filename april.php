<p>Leo is a zodiacal constellation whose prominent stars have been included in
many different mythologies. Stars depicting the head of Leo form the
‘Sickle’ asterism. The Leonids meteor shower radiate from this
constellation around mid-November.</p>

<p>The brightest star in the constellation is Regulus and is situated at the
base of the Sickle. At 2130 it has traveled 10-ish degrees past (to the right
of) due South. Equidistant and to the left of due South is the bright star
Denebola. Move five degrees to the right from this star and you will arrive at
two stars of equal brightness directly stacked on top of one another. These are
Zosma and Chertan. From the latter, the lower of the two stars, take a pair of
binoculars and aim them just below and to the left of this star, and if your
skies are dark enough you may be able to detect the faint smudges of light
known as the Leo Triplet – these are galaxies that are many thousands of
light years distant.</p>

<p>Two of the brightest smudges are M65 and M66. They are separated by a small
distance (in the line of sight) and can be observed in the same low
magnification field of view along with NGC 3628. Together, these three galaxies
make up the Leo Triplet. The sight of this lovely trio is a delight under low
power in telescopes. M65 is quite large and oval shaped and M66 has a
noticeably bright star-like nucleus. A moderate aperture telescope begins to
reveal subtle detail.</p>

