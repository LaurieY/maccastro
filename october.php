<p><em><strong>The Stars and Constellations</strong></em></p>

<p>There’s a star you should definitely look for during this month, called
Fomalhaut. To help find it start with the Square of Pegasus in the north. This
is a large obvious starry box-shape east of the Summer Triangle. A line due
East from Altair will put you smack in the middle of the Square. Four bright
stars mark its four corners. We will use these stars as pointers.</p>

<p>Imagine a line from the stars Scheat through Markab and extend it South to
Fomalhaut.</p>

<p>Fomalhaut is the only bright star in the constellation of Piscis Australis
(the Southern Fish). The constellation is small and rather faint, but Fomalhaut
is very interesting … It is a very white star (type A) only 22 light-years
away so we’re practically neighbours and is 13 times more luminous than our
Sun! To our eyes it shines at magnitude of 1.2. A few years ago astronomers
discovered a cloud of cool matter around Fomalhaut. This dust ring may be
similar to the accretion disk from which our Solar System formed.</p>

<p>This star doesn’t appear for very long during the year, so it’s
definitely worth hunting down, if for nothing more than ticking it off your
observing list …</p>

<p>There are a couple meteor showers this month but they are not very active,
however, you might want to look for them anyway. The Draconids are extremely
variable but usually weak. The shower only lasts one day – October 9th –
and is the result of the parent comet Giacobini-Zinner. The Orionids start
around October 15 and peak on the 21st and whittle away by the 26th of this
month. This is usually the best shower of October with as many as 25 meteors
seen each hour during the peak. These meteorites are bits of Halley’s comet.
At the end of the month the Taurids begin, but even at their peak in early
November, they produce only about 10 meteors an hour. These meteors are lovely
to watch as they tend to move gracefully across the sky, not as fast as most
other meteor debris.</p>

<p><em><strong>Perseus – this month’s sky highlight</strong></em></p>

<p>Perseus is the mythological hero who saved Andromeda from Cetus the Sea
Monster. Perseus used Medusa’s head (lopped off in a previous adventure) to
turn Cetus to stone. One of the best meteor showers (on August 12) radiates
from Perseus.</p>

<p>To find our hero, step outside at 2230 and look due East. Place your gaze
approximately 60 degrees high in the sky where you should see a bright star
called Mirfak. It forms the centre of a string of stars running in an 11
o’clock, five o’clock direction. This is the constellation of Perseus.</p>

<p>One of the most lovely star clusters lies just outside its boundary. To find
it (it is visible with the naked eye under dark skies) go back to Mirfak. Take
the stars that extend in the 11 o’clock direction for a distance of 11
degrees. Now place a pair of binoculars in this region (you might find it
easier to use your binoculars as you travel from Mirfak). You will hopefully
see a milky patch of light – this is the famous Perseus Double Cluster (NGC
889 and 864). Whenever it is visible I always visit this object with my
telescope because it is truly stunning. It is quite simply one the night
sky’s finest jewels. You are seeing a pair of bright and large open clusters
embedded in the faint glow of the Milky Way. The double cluster is visible
without optical aid but binoculars are required to separate the two clusters,
which are half a degree apart. A richfield telescope gives the best view of the
Double Cluster, with many stars of differing brightness visible. NGC 869 is
more tightly packed than NGC 884. Both clusters are about 7000 light years away
and are part of the Perseus arm, one of the spiral arms of our very own Milky
Way. The two clusters are a few hundred light years apart.</p>

<p><em><strong>In history …</strong></em></p>

<p>The first record of a solar eclipse was made by Ancient Chinese astronomers.
It occurred on October 22nd 2136 BC.</p>

<p>A piece of Mars fell on Chassigny (France) on October 3 1815. At that time
some people were still skeptical that rocks fell from the sky, especially so if
you had said it had come from Mars! The origin of this important meteorite was
not determined until a few decades ago through the use of isotope analysis.</p>

<p>Edwin Hubble discovered the first Cepheid variable in the Andromeda Galaxy
on the night of October 5 1923. He used its data to prove that the Andromeda
nebula, as it was then known, was actually a very distant star system outside
of our own Milky Way.</p>

<p>On October 4 1957 the USSR put Sputnik 1 into orbit. This was the first
man-made object to be placed in Earth orbit.</p>

<p>On October 18 1958 Luna 3 returned the first photos of the Moon’s far
side. As we all know, we cannot see that side of the Moon from Earth, so when
we saw the images sent from the spacecraft, it was like we were seeing a whole
new Moon.</p>

<p>Venera 9, another USSR space probe, sent back the first picture from
Venus’s surface on October 22 1975. It showed a world in which all the rocks
looked orange due to the refraction of light through Venus’s clouds. It was
determined that the surface of Venus had a temperature over 480 degrees C and
an atmospheric pressure about 90 times that of the Earth!</p>

