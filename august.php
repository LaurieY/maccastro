<p><em><strong>In history …</strong></em></p>

<p>Saturn’s moon Enceladus was discovered on August 28 1789 by William
Herschel.</p>

<p>On the night of August 5 1864, Giovanni Donati made the first spectroscopic
observations of a comet. He found mysterious bands in this comet’s spectrum
that we now know are produced by molecular carbon.</p>

<p>On August 18 1868 Norman Lockyer discovered helium in the Sun’s spectrum.
Actually, on that day he discovered the bands of an unknown element in the
Sun’s spectrum and named that new element “helium” after the sun-god
“Helios”. Years later, earthbound helium was isolated and its spectrum
confirmed Lockyer’s discovery.</p>

<p>Asaph Hall of the US Naval Observatory discovered Mars’ small satellite,
Deimos, on the August 11 1877. Six days later, August 17, 1877, he found
Mars’ other moon Phobos!</p>

<p>The first photograph of the Earth was taken from orbit by Explorer 6 on
August 7 1959.</p>

<p>On August 7 1996, NASA scientists announced that they had found structures
and chemicals in the Martian meteorite ALH84001 suggesting these could be
Martian micro-fossils. This ‘evidence’ has been disputed and most experts
in the field believe the data is inconclusive.</p>
