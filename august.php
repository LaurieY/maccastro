<p><em><strong>Sagittarius, the Archer – this month’s sky
highlight</strong></em></p>

<p>Sagittarius the Archer is a zodiacal constellation that contains the
‘Teapot’ asterism.</p>

<p>To view Sagittarius you’ll need a very open Southern horizon. This is not
the easiest of observations to make, but if you can succeed, it’s well worth
the effort. Let’s start at 2130. It’s still not dark, but at this time the
constellation is directly due South. The teapot we’re looking for is only 10
degrees above the horizon. Firstly find Altair, the lowest component of the
Summer Triangle, as described last month. With this star in your vision, move
straight down approximately 10 degrees, which is the equivalent to a fist held
vertically at arms length in front of you. Here you should find a faint-ish
star called Eta Aquilae. Now travel in a five o’clock direction for 30
degrees and you should see a wide grouping of stars in line with the direction
you’re moving. The brightest star here is called Nunki and forms part of the
handle of the teapot.</p>

<p>Using binoculars, go from Nunki to the left, in a three o’clock direction
for 15 degrees. As you move along you should sweep up a large-ish, elongated
misty patch. This is M8, the Lagoon nebula. It is a magnificent emission nebula
with an embedded open cluster. This cluster of young stars is heating the
nebula’s gas and causing it to emit light. In binoculars the dark lane (the
lagoon) that divides the nebula’s brighter regions and gives this object its
name might be apparent, but it is very much dependant on sky brightness. A
small telescope begins to reveal this nebula’s intricate folds and dark
regions amidst brighter areas. Dark ‘Bok globules’ in the nebula mark dense
clouds of gas and dust which are the site of star formation. The Trifid nebula,
M20, lies close (above and to the right of) M8 and both nebulae can be seen in
the same binocular field of view.</p>

<p>The Trifid Nebula gets its name from the rifts of dark matter that trisect
its southern region. The Trifid lies in a rich area of the Milky Way, with M21
(to the left in a 10 o’clock position) in the same binocular field of view.
This makes a stunning sight and is best seen at low power. M20 and M8 may be
physically connected, although this is uncertain. The Trifid Nebula is a hot
red emission nebula of hydrogen gas surrounded by a blue reflection nebula made
up of dust grains. Don’t expect to see this colour with your eyes – it’s
only apparent in long exposure images.</p>

<p>Let’s end our tour at M21. This bright cluster contains about 60 stars
that are readily visible in a small telescope. M21 has a strong concentration
of stars towards its centre and makes an impressive sight in binoculars along
with M20 and M8. In larger telescopes, a ‘diamond ring’ of 10 or so stars
can be seen. This is an extremely young cluster in astronomical terms at only
five million years old.</p>

<p>If you have succeeded in this observation, give yourself a pat on the back!
Finally, when you are ready to leave Sagittarius take note that you have just
spent time looking into the heart of our own galaxy, the Milky Way.</p>

