<p><em><strong>The Perseids</strong></em></p>

<p>On any dark night, you should be able to see about ten naked-eye meteors per
hour.</p>

<p>However, when the Earth passes through a comet’s tail or the debris left
by a periodic comet, the number of meteors seen is greatly increased. We enter
cometary debris during the last week in July and continue to pass through for a
couple more weeks.</p>

<p>When the debris enters the Earth’s atmosphere they become meteors. They
charge at the Earth at very high speed (72 kilometers per second) producing a
great deal of heat by friction. This heat is so intense that it ionizes the
atoms and molecules in the air surrounding the meteor, causing it to glow by
incandescent light. (This light is the same as produced in an incandescent
light bulb.) An average meteoroid becomes a meteor at about 115 kilometers
above the Earth’s surface and it usually burns out at an altitude of about 70
kilometers as it breaks up into tiny particles called micro-meteors.</p>

<p>The name of the meteor shower that will occur during this month is called
the Perseids – otherwise known as ‘Old faithful’ because it never fails
to impress.The best time to view the show is after local midnight as the
radiant, from which the meteors appear from, is starting to climb high into the
sky.</p>

<p>The meteors appear to radiate from a single point of the sky. This happens
because the Earth intercepts the meteoroids in a series of parallel paths. To
the observer, this point is like looking at a railway line as it appears to
meet in the distance. The best time to view the Perseids is at their peak, on
the night of August 11-12, when you can expect to see nearly one Perseid meteor
a minute. However, if the weather looks better a few days either side of the
peak date, it is worth observing then too.</p>

<p>The shower will appear from a point in the constellation of Perseus. This
constellation lies just below and to the left of Cassiopeia.</p>

