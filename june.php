<p><em><strong>The Stars and Constellations</strong></em></p>

<p>The trick to learning star identification is …</p>

<p>1. Take is slow and easy. Try to learn only a few stars at a time –
certainly no more than a dozen in one night, otherwise you’ll feel
overwhelmed.</p>

<p>2. Learn the brightest stars first. Not only do these really stand out but
you can see them even when the sky conditions are less than perfect (such as
when you are in a city or when the Moon is full).</p>

<p>The summer months are a great time to learn and identify at least the
brightest stars. Use the night sky maps above (why not download one and print
it out?) to help you.</p>

<p>There are three particularly bright stars in the South eastern sky during
the early Summer months – Vega, Deneb and Altair. Vega, a beautiful
blue-white star, is located in the constellation of Lyra. Deneb can be found in
Cygnus (the Swan), otherwise known as the Northern Cross. Just South of this
lies the huge nebulous complex NGC 7000, the North American nebula –
so-called because its shape has an uncanny resemblance to the country. Altair
is the brightest star to be found in Aquila (the Eagle). This constellation is
a superb guide for discovering some of the brightest star clusters, and
portions of the Summer Milky Way towards Sagittarius and the centre of our own
Galaxy.</p>

<p></p>

<p><em><strong>Hercules – this month’s sky highlight</strong></em></p>

<p>Hercules, an ancient constellation, contains the well known trapezoidal
asterism ‘The Keystone’ and the beautiful globular cluster M13. In
binoculars M13 looks like a fuzzy star and is one of the more spectacular
objects in the sky.</p>

<p>We have to wait until 2330 before Hercules is due South. The centre of the
constellation is no less than 70 degrees high in the sky. It’s stars aren’t
overly bright, but if you can locate Vega (as described next month, or using
the information above) and Arcturus (as described in May’s sky highlight),
Hercules is between the two. Look for a series of four stars that resemble a
keystone – i.e. the central stone in a archway. Just above halfway in a
direct line between the right-hand most stars you will find M13.</p>

<p>The Great Hercules Cluster is one of the sky’s most precious jewels and
the finest globular cluster in the northern hemisphere. Visible to the naked
eye under dark skies, M13 looks like a fuzzy ball of light in binoculars. A
moderate sized telescope and high magnification shows a blazing ball of stars
with many resolved members. The Great Hercules Cluster contains about 400000
stars, spread across 140 light years of space. The star density near the
cluster’s centre is extremely high, with stars separated by only a few
astronomical units. M13 was originally discovered by Edmond Halley, better
known for predicting the return of the comet that still bears his name. M13 was
selected as a target for one of the first radio messages broadcast to
extraterrestrials from the Arecibo observatory in Puerto Rico. If alien
civilizations exist in the Great Hercules Cluster, we won’t receive their
reply for 50000 years, as M13 is 25000 light years from Earth.</p>

<p></p>

<p><em><strong>In history …</strong></em></p>

<p>On the night of June 24 1881, William Huggins became the first person to
photograph the spectrum of a comet. He discovered that it contained cyanogen
(CN) – a deadly gas. This knowledge initiated mass hysteria 29 years later
when the Earth passed through the tail of Halley’s comet!</p>

<p>On June 30 1908, something exploded over a part of Siberia called Tunguska.
It is believed to have been an asteroid or comet that created an air-burst a
few miles above the ground. This was caused by the incredible heat that built
up during its brief passage through the atmosphere. The force of the blast
flattened dense forest for miles.</p>

<p>Valentina Tereshkova, aboard the Vostok 6 spacecraft, became the first woman
in space on June 16, 1963</p>

<p>On June 6 1971, Georgi Dobrovolsky, Vladislav Volkov and Viktor Patsayev
docked with the Salyut space station. Twenty three days later (30 June), after
a successful mission in space, all three died during re-entry when their Soyuz
11 spacecraft rapidly lost pressure.</p>

<p>James Christy, of the US Naval Observatory in Flagstaff Arizona, discovered
Pluto’s satellite, Charon, on the night of June 2 1978.</p>

<p>Pioneer 10 became the first spacecraft from the planet Earth to leave the
Solar System on June 13, 1983.</p>

