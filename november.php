<p><em><strong>The Stars and Constellations</strong></em></p>

<p>Speaking of pointers, you can use the arc of the handle of the Plough to arc
all the way to the bright orange star Arcturus – not far from the Eastern
horizon. </p>

<p>Of course, the Great Summer Triangle, comprised of the three bright stars
Vega, Deneb and Altair, is also pretty obvious. During November you’ll find
it directly overhead just after sunset. </p>

<p>It is worth noting that neither the Plough, Little Bear, or the Summer
Triangle are true constellations. The International Astronomical Union (IAU)
recognizes only 88 and all are named after mythical characters or inanimate
objects. Instead, these star patterns are called asterisms. </p>

<p>When people talk about constellations, lots of people merely think of the
Zodiac. This is a series of constellations positioned along the same plane as
the Earth’s orbit so the Sun and planets seem to travel through them. There
are a dozen of these constellations (13 if you count Orphiucus, which isn’t
part of the Zodiac, but falls within the same area of the sky) and six are
visible soon after sunset this month. They form a line that runs from the
Southwest to the Northeast. </p>

<p>Can you spot them? They are: Scorpius, Sagittarius, Capricornus, Aquarius,
Pisces and Aries There you have it – half the Zodiac! </p>

<p>If you stay up late enough others can also be seen: Taurus, Gemini, Cancer
and Leo. Virgo and Libra are both obscured by the Sun.</p>

<p>The Taurid meteor shower reaches its peak on November 3rd but you’ll see
less than ten an hour. Not too impressive. These meteors are the
‘offspring’ of Comet Encke. However, if you do see one, they are beautiful
– usually bright and slow-moving. </p>

<p>The Leonids should be more interesting. This meteor shower is caused by the
remains of the comet Tempel-Tuttle. This shower last only a few days, starting
around November 15, peaking on the 17th and tailing off by the 19th. Don’t
expect to see storm, or enhanced levels of activity as in previous years.
Instead, you’ll need to be on your toes when they streak into our atmosphere
because they’re mighty quick – in fact they’re the fastest meteors we
know associated with a major shower traveling at a massive 71 km per second!
</p>

<p><em><strong>The Pleiades, the Seven Sisters</strong></em> – this month’s
sky highlight.</p>

<p>The Pleiades is the most famous of all open star clusters, containing around
500 members set against a black velvet sky. This young first magnitude open
cluster is easily visible to the unaided eye and resembles a smaller version of
the Big Dipper. At least six hot blue stars are readily visible and keen eyed
observers can see more. Because of its large diameter, two degrees, M45 is best
seen in binoculars. A faint veil of nebulosity surrounds the brightest Pleiades
members. However you will need a dark, clear sky to see the most easily
observable patch – the Merope Nebula (IC 349), which surrounds the star of
the same name. These reflection nebulae are not remnants of the gas cloud where
the Pleiades was born, but a chance cloud of dust that the cluster is passing
through. In some ancient cultures, ceremonies to honour the dead were held on
the day when the Pleiades reached its highest point in the sky at midnight
(this is around Hallowe’en). Ancient Aztecs believed the Pleiades would be
overhead at midnight the day the world ended. </p>

<p>It’s very easy to find the star cluster. Take yourself outside at 2330 and
look South. Look 60 degrees in elevation and you will see a small patch of
stars (these may look a little fuzzy if your eyesight isn’t perfect). It’s
as simple as that. As a challenge why not try to count how many stars you can
see in the cluster? It’s a good test of the quality and darkness of your
night sky. If you can count upwards of six you’re not doing too badly. If you
can get towards nine you’re doing very well indeed! </p>

<p>Finally, take a pair of binoculars and take a long look at what has to be
one of the most beautiful sets of jewels in the night sky. Take particular note
of the wonderful string of pearly stars that extends down on the left-hand side
(the orientation is likely to be different in a telescope). </p>

<p><em><strong>In history … </strong></em></p>

<p>On November 30, 1954 Elizabeth Ann Hodges was enjoying a nap in Sylacauga,
Alabama when she was hit by a 5 kg meteorite! She suffered a bruised hip. </p>

<p>The first Earthling in space, a dog named Laika, was placed into orbit by
the USSR aboard Sputnik 2 on November 3 1957. Unfortunately, there was no plan
to bring Laika back so she also became the first Earthling to die in space. </p>

<p>The USA space probe Mariner 9 become the first man-made object to orbit Mars
on November 13 1971. This mission forever changed our view of the red planet.
It had been estimated that the air pressure on the Martian surface would be
about 87 millibars but Mariner 9 showed it to be less than 10. We expected the
Martian atmosphere to be mostly nitrogen, like our own, but it turned out to be
mostly carbon dioxide. The polar ice caps were not the water ice as we had
expected but mostly dry ice (frozen carbon dioxide). Mariner 9 sent back over
7,000 images of the planet during the next eleven months. </p>

<p>On the night of November 1 1977 Charles Kowal discovered Chiron. This icy
asteroid was the first of a population of objects orbiting in the outer Solar
System. </p>

