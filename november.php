<h4 style="text-align:center;margin-left:auto;margin-right:auto;">The night sky
in November</h4>

<p><em>Want to know what's up in the sky tonight? Our night sky chart will
provide you with the information you need to locate the brightest planets,
stars and deep-sky objects ...</em></p>

<p>This month we have a couple of meteor showers. There are also some lovely
stars and constellation to see too … </p>

<p>The Plough is obvious, but you’ll need a good (moonless and cloudless)
night to see all the stars that make up the Little Bear.</p>

<p>Regardless, even on an average night you can find Polaris in the end of the
Little Bear’s handle by using the stars Merak and Dubhe in the Plough as
pointers to it.</p>

