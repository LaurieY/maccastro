<p><em><strong>A variable star project</strong></em></p>

<p>Throughout the month of March, Algol is nearly overhead at sunset and
visible the entire evening. This star is easy to overlook because it’s not
nearly as bright as some stars in the night sky but it is Algol’s brightness
that makes it so unusual and fascinating. Algol’s brightness changes making
it a so called ‘variable star’. There are many different reasons for a star
to change its brightness, this one does so because it is regularly eclipsed by
a darker companion orbiting around it. Algol is an eclipsing variable.</p>

<p>During eclipse Algol dims from a magnitude of 2.1 to 3.4. It takes the
eclipsing binary star exactly 2 days, 20 hours, 48 minutes and 56 seconds to
complete its orbit. Each eclipse lasts about 10 hours but it’s during the
middle 2 hours that the maximum eclipsing occurs.</p>

<p>This is a fascinating event to watch but, of course, the first thing you
have to do is find Algol. These directions will help you (use our monthly sky
map above).</p>

<p>Imagine a line from the very bright star Capella to the not too bright stars
of Almaak, Mirach and Alpheratz. There is a star on either side of the line
between Almaak and Capella, about midway between them. The one to the north is
Mirphak but the one to the south is our target – Algol (don’t confuse it
with the brighter star Hamal and don’t confuse any of these with Saturn and
Jupiter which would be visible farther south than these stars!</p>

<p>Now that you know where it is, you’ll want to know when to watch for Algol
dimming. It the best way is to watch over consecutive nights and compare Algol
to the brightness of the stars around it. Use Almaak, which always has a
magnitude of 2.1 (Algol’s normal magnitude), as a reference. At minima, Algol
will be as faint as either of the two stars flanking it.</p>

<p>Good luck! we would love to hear from you if you are successful at observing
Algol’s minima.</p>

