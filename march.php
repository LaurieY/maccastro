<p><em><strong>Auriga – this month’s sky highlight</strong></em></p>

<p>Auriga was named after the mythological inventor of the chariot. Auriga
contains the sixth brightest star in the sky, Capella, a binary star system
whose components are thought to be red giant stars.</p>

<p>Look due West at 2100 and at an elevation of 60 degrees and you will see
Capella a beautiful yellow-orange star. About five degrees below in a seven
o’clock position, you will find an arrow shaped arrangement of stars pointing
towards the position of two o’clock . This asterism is affectionately known
as ‘The Kids’.</p>

<p>Using a star atlas try to locate the three famous Messier objects within
this constellation: M36, M37 and M38.</p>

<p>M36 is a nice open cluster embedded in the faint radiance of the Milky Way.
It contains a dozen or so bright members against a hazy background of fainter
stars. Because it lies against the Milky Way, it’s hard to discern the edges
of this loosely packed cluster. In binoculars, M36 along with M38 and M37 can
be seen in the same field of view. M36 is a young cluster (only 30 million
years old), so it contains no red giant stars.</p>

<p>Under very dark skies M37 is a naked eye cluster. It is a joy to observe and
undoubtedly the best open cluster in Auriga and a favourite amongst observers.
Dozens of bright stars can be resolved. Fainter white stars surround a red 9th
magnitude star near the centre of this cluster, adding to its aesthetic beauty.
A telescope and careful observation reveals several dark voids within the
cluster, which are dust lanes of the Milky Way.</p>

<p>M38 is a large open cluster with many bright and fainter members. This
populous cluster is easily seen in binoculars along with nearby NGC 1907, a
smaller 8th magnitude open cluster. A combination of brighter stars, double
stars and dark lanes make it a delightful site. M38 is relatively old for an
open cluster, having formed 220 million years ago.</p>

