<p><em><strong>In history …</strong></em></p>

<p>Clyde Tombaugh discovered Pluto on February 18 1930. He was examining some
telescopic images he had taken weeks earlier using a 13 inch telescope at the
Lowell Observatory.</p>

<p>Miranda, the largest moon of Uranus, was discovered by Gerard Kuiper on
February 16 1948.</p>

<p>On February 20 1962, Lt. Colonel John H. Glenn Jr. became the first American
in orbit. (Alan Shepard’s flight nine months earlier was sub-orbital but
still in “space” so Shepard was the first American in space.) Glenn spent
nearly 5 hours in orbit as he zoomed around the Earth three times in his
Mercury-Atlas 6 capsule. (In 1998 Glenn returned to orbit via the space shuttle
Discovery and, at the age of 77 years, became the oldest space traveler.)</p>

<p>The USSR placed Luna 9 on the surface of the Moon on February 3 1966 –
making it the first spacecraft from Earth to (soft) land on another world.</p>

<p>Jocelyn Bell discovered the first pulsar on February 24 1968.</p>

<p>On February 61971 Alan Shepherd proved that, while wearing a space suit,
it’s impossible to swing a golf club gracefully as he whacked a golf ball on
the Moon during the Apollo 14 mission.</p>

<p>On the morning of February 1 2003, 7 NASA astronauts died when the shuttle
Columbia broke up during re-entry. Evidence indicated that during lift-off a
piece of ‘foam’, insulating the external fuel tank, broke off damaging the
port wing. This damage lead to structural failure during the re-entry.</p>

