<p><em><strong>The Stars and Constellations</strong></em></p>

<p>There are many bright stars in the east and they will be visible until the
small hours of the morning. Sirius is the brightest star in our night sky, with
its magnitude of -1.1. This star is 25 times brighter than our Sun but, because
Sirius is so far away (8.6 light years, or about 77 million million
kilometers), it appears much dimmer. Sirius is in the constellation of Canis
Major, the Big Dog, and has the nickname ‘The Dog Star’. If you have
trouble finding Sirius, take note on the night sky chart that the three stars
in Orion’s belt point southeast directly to the star.</p>
