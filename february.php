<p><em><strong>Gemini, the Twins – this month’s sky
highlight</strong></em></p>

<p>Gemini is a zodiacal constellation whose primary stars are named after the
mythological twins Castor and Pollux, the sons of Zeus and Leda and the
siblings of Helen of Troy. In Chinese mythology the twin stars represented Yin
and Yang.</p>

<p>Looking South at approximately 2100 you will see the brightest star in the
sky, Sirius about 20 degrees above the horizon. If you double this elevation
and look in the direction of 11 o’clock you will find the star Procyon.
Travel the same distance again vertically and you will see two stars of equal
brightness in a seven o’clock, one o’clock orientation. These are the Twins
Castor (the uppermost star) and Pollux.</p>

<p>Look carefully at these two stars. Can you discern the difference in colour
between the two? I won’t tell you which is which, but all I’ll say is that
one is a pale orange and the other is a white-ish blue.</p>

<p>Now imagine an equilateral triangle pointing to the right, away from Castor
and Pollux. Take a pair of binoculars and place the field of view at the point
of the triangle and you will see a lovely elongated group of stars containing a
colourful pair called 64 and 65 Geminorum. The colour difference between these
two stars should be easier to distinguish than those of the Twins.</p>

