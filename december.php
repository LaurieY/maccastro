<p><em><strong>The solstice and shooting stars</strong></em></p>

<p>The Winter Solstice occurs on December 21st. Solstice is the day of the year
when there is maximum daylight at one hemisphere and minimum daylight at the
other. Solstice occurs twice a year. The day of the year midway between the two
Solstices is called the Equinox and on that day all parts of the Earth
experience equal amounts of daylight and night time (12 hours of each).</p>

<p>The time of minimum sunlight for a hemisphere is called the Winter Solstice.
For us in the Northern Hemisphere it occurs around December 22nd but in the
Southern Hemisphere the Winter Solstice occurs six months later around June
21st.</p>

<p>On the day of the Winter Solstice the Sun (if we get to see it!) appears to
be as far from the celestial equator as possible, causing it to spend less time
in the sky, which results in longer night time. On that same day, in the
opposite hemisphere, the Summer Solstice occurs resulting in the longest day
time of the year.</p>

<p>As the earth moves from Winter Solstice to Summer Solstice the daylight
grows longer and the Sun rises higher in the noon sky.</p>

<p>Eventually the day time is equal to the night time. At this point we
experience the Spring (or “vernal”) Equinox. As the Earth continues in its
orbit the daylight gets longer and longer until we reach the Summer Solstice.
After the Summer Solstice the day length starts to slowly decrease again. Three
months later (or six months after the Spring Equinox) the Earth’s tilt is
again perpendicular to the Sun’s rays and we have the Autumnal Equinox.</p>

<p>It is the tilt of the Earth that defines our seasons. Some people mistakenly
believe that the cold winter days are caused by the distance of the Earth from
the Sun. Nothing could be farther from the truth. Indeed, you may be surprised
to know that the Earth is closest to the Sun in January!</p>

<p>On December 22nd the Winter Solstice is upon us and brings the longest night
for astronomers, so bundle up warm and get outside and enjoy this special
night. You should of course, get out on any other clear night, especially
during early-mid December to see some spectacular meteors …</p>

<p>The Geminid meteor shower starts on December 7 and peaks on the night of the
13th and finish by December 16th. This is a very rich meteor shower with up to
75 ‘shooting stars’ seen per hour during the peak. These meteors are the
dusty debris of an asteroid called Phaethon. Do watch out for them because they
are particularly beautiful – often bright and slow moving.</p>

<p><em><strong>The Plough – this month’s sky highlight</strong></em></p>

<p>This is the most famous and recognizable asterism in the Northern
hemisphere, located in the constellation Ursa Major. The Plough is formed by
the stars Dubhe, Merak, Phecda, Megrez, Alioth, Mizar and Alkaid.</p>

<p>Although not the best time of the year to observe it, it’s orientation
during December makes it look a lot less like a Plough than you’d expect.</p>

<p>At 2330 look Northeast at an altitude of 35 degrees. It might be a good idea
to ensure you are standing away from houses as they may well obscure the stars
of the constellation because of it’s position quite low in the sky. When you
see it you will notice that the Plough looks as if it has been toppled over and
is standing on its handle. This is simply because of the Earth’s tilt (23.5
degrees) and position in its orbit around the Sun. If you make a mental note to
look in the month of May at the same time you will see that it is now standing
on the opposite side of the sky in the opposite orientation! The changing
positions of all the constellation is also linked to the rotation of the Earth.
If you can drag yourself out of bed at 0430 and look directly North at an
altitude of 83 degrees, the Plough is now upside down. If you could now
teleport yourself into the Southern hemisphere, in say, South Africa, the
constellation would now appear the right way around … How perplexing!</p>

<p>No matter when you observe it, it’s worth looking for the famous naked-eye
double star Mizar and Alcor. If you can see the bottom of the Plough’s handle
just move one star into the rest of the handle. This is Mizar. Can you see very
close to it a fainter companion? This is Alcor, shining at magnitude four. If
you have access to a telescope point it at these two stars. What do you see? No
doubt you’ll realise that Mizar (the brightest star) is itself multiple.
Depending on the magnification you’re using you may see three stars. This
clearly demonstrates that the deeper we gaze into the Universe the more
surprising our discoveries are.</p>

<p><em><strong>In history …</strong></em></p>

<p>On the night of December 23rd, 1672, Giovanni Cassini discovered Saturn’s
moon Rhea.</p>

<p>In 1845 on the night of December 18th, Hencke discovered the asteroid
Astraea.</p>

<p>Francis Pease used the interferometer at the Mount Wilson observatory to
make the first measurements of a star’s diameter (other than the Sun’s) on
the night of December 13th, 1920.</p>

<p>On December 24th, 1968 Frank Borman, James A. Lovell Jr., and William A.
Anders became the first humans to orbit another world – the Moon! The Apollo
8 Module took 20 hours to complete 10 orbits, and then returned safely to our
home world.</p>

<p>On December 15th, 1970 the USSR’s Venera 7 became the first spacecraft to
successfully soft land on another planet – Venus.</p>

<p>On December 3rd, 1971 the USSR’s Mars 3 became the first spacecraft to
make a soft landing on Mars.</p>

<p>Manned exploration of the Moon ended at 22:54:37 UT on December 14th, 1972,
when Apollo 17 Commander Eugene A. Cernan and Lunar Module Pilot Harrison H.
Schmitt lifted off from the Moon.</p>

<p>The US’s Pioneer 10 flew past Jupiter on December 3rd, 1973 and about a
year later Pioneer 11 followed.</p>

