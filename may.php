<h4 style="text-align:center;margin-left:auto;margin-right:auto;">The night sky
in May</h4>

<p><em>Want to know what's up in the sky tonight? Our night sky chart will
provide you with the information you need to locate the brightest planets,
stars and deep-sky objects ...</em></p>

<p>During early May, the Earth passes through a debris trail left by comet
Halley. This results in a meteor shower is called the Eta Aquarids. They are so
called because the meteors appear to come from part of the constellation of
Aquarius. (In October we pass through another section of Halley’s debris path
in a shower called the Orionids.) </p>

<p>At their peak on May 5, the Eta Aquarids may produce up to 30 meteors per
hour.</p>

