<p><em><strong>The Stars and Constellations</strong></em></p>

<p>The sky map shows the brilliant constellation of Orion is seen in the south.
Moving up and to the right – following the line of the three stars of
Orion’s belt – brings you to Taurus; the head of the bull being outlined by
the V-shaped cluster called the Hyades with its eye delineated by the orange
red star Aldebaran. Further up to the right lies the Pleiades, or Seven Sisters
star cluster. Towards the zenith from Taurus lies the constellation Auriga,
whose brightest star Capella will be nearly overhead. To the upper left of
Orion lie the heavenly twins, or Gemini, their heads indicated by the two
bright stars Castor and Pollux. Down to the lower left of Orion lies the
brightest star in the northern sky, Sirius, in the constellation Canis Major.
Finally, up and to the left of Sirius is the bright star Procyon in Canis
Minor.</p>

<p><em><strong></strong></em></p>

<p><em><strong>Orion, the Hunter – this month’s sky
highlight</strong></em></p>

<p>Orion, perhaps the most beautiful of constellations, will be seen directly
due South at around 2130 during January. I’m not going to tell you how to
locate it. Its distinctive, its abstract human stick-figure form can’t be
missed!</p>

<p>Orion is the hunter holding up a club and a shield against the charge of
Taurus the Bull up to his right. Alpha Orionis, or Betelgeuse, seen at top
left, is a red super giant star. Beta Orionis, or Rigel, lower right, is a blue
super giant, which, at around 1000 light years distance is about twice as far
away as Betelgeuse. It has a 7th magnitude companion only visible in telescopes
of good quality. The three stars of Orion’s belt lie at a distance of around
1500 light years. They comprise of Alnitak, Alnilam and Mintaka, which are
equally spaced and nearly in a straight line. Just below the lower left hand
star lies a strip of nebulosity against which can be seen a pillar of dust in
the shape of the chessboard knight. It is thus called the Horsehead Nebula. It
shows up very well photographically but is exceedingly difficult to see
visually – even with relatively large telescope.</p>

<p>Beneath the central star of the belt lies Orion’s sword containing one of
the most beautiful sights in the heavens – The Orion Nebula, M42. It is a
region of star formation and the reddish colour seen in photographs comes from
hydrogen excited by ultraviolet emitted from the hot, bright blue stars. The
Hubble Space Telescope has found protoplanetary disks of gas and dust around
some of these stars. These disks are about twice the size of our solar system,
and may eventually condense to form extrasolar planets. M42 is a veritable
catalog of different object types, including multiple stars and reflection plus
emission nebulosity. </p>

<p>Try to view the nebula on every possible occasion with any type of optical
instrument as well as with the naked eye. The wealth of detail visible in it is
simply outstanding. Intricate wisps, shapes and the contrast between brighter
and darker regions never ceases to amaze.</p>

