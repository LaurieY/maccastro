<p><strong>Joining Macclesfield Astronomical Society</strong></p>

<p>If you wish to become a member of Macclesfield Astronomical Society you can
request an application form from any committee members at our meetings or by
emailing us. Subscription renewals are due on the 1st of September and run for
12 months. Membership subscriptions can be paid using a personal cheque, cash
or by BACS.</p>

<p><strong>Meetings</strong></p>

<p><em>There are 3 official meetings held by Macclesfield Astronomical Society
each month.</em></p>

<p>Our <strong>Workshop</strong> (Calvary Church, Macclesfield) is held on the
first Tuesday of every month. This is where our members showcase their passion
for astronomy in a variety of short presentations. We now start this meeting
with a session for those new to astronomy.</p>

<p>Our <strong>Social Evening</strong> (Blacksmiths Arms, Henbury) is held on
the second Tuesday of every month. This is our chance to get together and build
camaraderie which makes astronomy special. </p>

<p>Our <strong>Lecture</strong> (Village Hall, Goostrey) is held on the third
Tuesday of every month. Our speakers are usually well regarded academics or
experts in the world of astronomy.</p>

<p>See our Forthcoming events page for further details. All meetings begin at
20:00 and last approximately 90 minutes.</p>

<p></p>

<p><strong>Dark-sky field trips</strong></p>

<p>Our weekend Dark Sky Field Trips are an excellent, enjoyable and sociable
way to experience good dark sky conditions, whether observing visually, with
binoculars, telescopes or with photographic equipment. Cloudy conditions offer
opportunity for relaxation, reading and research with plenty of astronomy
discussion, information and mentoring including occasional videos, and
talks.</p>

<p>Daytime activity during the Field Trips includes solar and atmospheric
observing, walking, visits to local attractions, lounging, reading, crosswords,
and socialising. The Field Trips are invariably very enjoyable, worthwhile and
good value in all respects, and those who come along are keen to come again.</p>

<p>Join us and see the night sky as Nature intended it to be …</p>

<p></p>

<p><strong>Outreach</strong></p>

<p>Our team of members lead by Chris Sweetman, Outreach Coordinator, volunteer
to support local groups of children such as cubs, brownies, scouts and primary
or secondary schools to further their interest and understanding in astronomy.
Our presentations support the national curriculum or help groups achieve their
astronomy badge.</p>

<p>We are also privileged be a contributor for the NHS Recovery College in
Macclesfield exploring the theoretical and practical application of astronomy
with discharged patients to aid them in their rehabilitation from mental
illness.</p>

