<p><strong>Members</strong></p>

<p>Whether your interest is visual, technical, historical, scientific or
spiritual you will find a kindred spirit within the Society. Many of our
members have considerable experience in many aspects of amateur astronomy such
as astrophotography and CCD-imaging. If you are considering buying a telescope,
talk to our experienced observers who will happily offer advice and guidance.
The Society owns several telescopes and binoculars, which are available for
hire by members. We also have an extensive book and video/DVD library from
which titles may be borrowed.</p>

