<p><strong>Our aims</strong></p>

<p>Our long-term aims are to provide a focal point for contact between local
astronomers and to make astronomy accessible to the general public. We would be
pleased to hold Astronomy evenings for local schools and youth groups, to
encourage interest in astronomy and to provide support for it as part of the
National Curriculum.</p>

