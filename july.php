<h4 style="text-align:center;margin-left:auto;margin-right:auto;">The night sky
in July</h4>

<p><em>Want to know what's up in the sky tonight? Our night sky chart will
provide you with the information you need to locate the brightest planets,
stars and deep-sky objects ...</em></p>

<p>During July the our planet Earth reaches its most distant position from the
Sun. Firstly, though you might want to try something I mentioned last month …
The Summer months bring to view a vast asterism in the sky called the ‘Summer
triangle’. Notice I said ‘asterism’, not constellation. An asterism is
simply a grouping of stars, which may form a memorable shape in the night sky.
Asterisms are not officially recognized by the International Astronomical Union
(IAU) in the way that constellation are.</p>

