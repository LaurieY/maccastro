<p><em><strong>The Stars and Constellations</strong></em></p>

<p>During July, look high in the SE and you will notice the icy-blue star
called Vega within the constellation of Lyra. It’s unmistakable because
it’s the brightest star in that region of the sky. It is definitely worth
noting that it is best to perform this observation at dusk – not when it’s
dark – so other bright-ish stars do not confuse you!</p>

<p>Moving on … Now you’ve found Vega, look towards the seven/eight
o’clock position from the star and you will see another bright star – this
is Deneb. Deneb is the brightest star in the constellation of Cygnus, the Swan.
This is one of my favourite constellations.</p>

<p>From Deneb, look towards the four to six o’clock position and you’ll
find Altair, the brightest star in the constellation of Aquila, the Eagle.</p>

<p>Congratulations, you’ve just found the third and final component of the
Summer triangle! If you don’t get chance to see it during July, don’t worry
because it will dominate the southern region of our night sky for some months
to come.</p>

<p>Finally, not very many people know where the name for this asterism came
from. It was in fact first coined by the inimitable astronomer Sir Patrick
Moore.</p>

<p><em><strong>Cygnus, the Swan - this month’s sky highlight</strong></em></p>

<p>Since you have located the Summer Triangle as described above, go back to
Deneb and we’ll explore Cygnus … This is a constellation well known in
northern summer skies as the “Northern Cross”. Located along the Milky Way,
Cygnus contains many objects for binoculars and the telescope, including the
North American Nebula and the beautiful double star, Albiero.</p>

<p>Firstly, locate its cross-shape. It’s easiest to trace from Deneb –
simply take a line in the direction of four o’clock from the bright star and
you will find three equidistant stars (of varying brightnesses) making the stem
of the cross. Now come back to the first star after Deneb. Trace another line
in a two o’clock, seven o’clock direction. Here you will find the cross
bar.</p>

<p>Go back to the fourth star of the stem. Using a pair of reasonably high
magnification binoculars take a look at the star. You should notice that it is
in fact two – a double star with blue and orange components. This is Alberio,
probably the most beautiful of it’s kind in the Northern hemisphere sky.
Through a telescope it will take your breath away.</p>

<p>One of my favourite past-times with binoculars is to simply sweep back and
forth through Cygnus. Looking in this direction we are looking directly into
the plane of the Milky Way (i.e. edge-on). If your sky is dark enough and
you’re away from city lights endless chains of stars, great rifts of darkness
and the subtle hint of literally millions of stars beyond the resolution of
vision can be seen. Spend a while here and imagine yourself journeying through
our galaxy.</p>

<p><em><strong>In history …</strong></em></p>

<p>It has been calculated that on July 4, 1054 a star in the constellation of
Taurus exploded as a supernova. It became extremely bright and was recorded by
Chinese astronomers. In fact, the star went supernova six thousand years before
1054, but it took that long for its light to reach us. Isn’t that
amazing?!</p>

<p>The remnants of the explosion can be seen today (with a good telescope) as
the Crab Nebula (M1), in Taurus.</p>

<p>On the night of July 16 1850 astronomers at the Harvard Observatory took the
first photograph of a star other than our Sun – Vega. Two weeks later, on
July 28, the first photograph of the Sun during a total eclipse provided the
first evidence of the Sun’s corona.</p>

<p>‘One small step for man. A giant leap for mankind’ occurred on July 20
1969 when Neil Armstrong set foot on the Moon. Edwin Aldrin soon followed Neil
down the ladder of the Lunar Excursion Module to become the second man to stand
on the Moon.</p>

<p>They collected about 20 kilograms of lunar rocks and soil during their stay
on the Moon before returning to the Command Module where the third member of
the Apollo 11 team, Michael Collins, had patiently waited orbiting the Moon.</p>

<p>On July 20 1976 Viking 1 landed on Mars and returned the first images taken
from the surface of the Red Planet.</p>

<p>The first fragments from Comet Shoemaker-Levy 9 began to impact Jupiter on
July 16 1994.</p>

