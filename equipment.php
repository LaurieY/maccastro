<p></p>

<p><em>However, most of us want to see details on the planets, closeups of the
Moon and galaxies galore. For that one will need a telescope. The good news is
that if you choose wisely, you can now get an excellent telescope for a
reasonable sum. One can get a scope that will perform well for around £200,
and a computer-controlled one that will find objects for you for under
£300.</em></p>

<p>Ask 10 astronomers, 'which is the best telescope?' will gather 10 different
answers, none of which is wrong. Many seasoned astronomers will have more than
one, indicating that there is no simple answer.</p>

