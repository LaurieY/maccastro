<h4>Choosing a telescope – a beginner’s guide</h4>

<p></p>

<p>It is said that all amateur astronomers want a telescope – and the bigger
the better. In my experience that isn't always true. Meteor observers prefer
the naked eye and Comet hunters prefer large binoculars. A large scope is
difficult to handle and may get rarely used. A smaller scope may not catch as
much light, but one will see 'more' as it will be used more often.</p>

