<p>To choose the right telescope, one needs more information.</p>

<p>Telescopes are compared by their aperture — the diameter of the main lens
or mirror. When astronomers refer to a ‘small’ telescope they mean one with
a small aperture. The aperture dictates how much light the telescope collects
– and the more light it collects, the more one can see. </p>

<p>Therefore to see more one buys the telescope with the largest aperture one
can afford. </p>

<p>Well no! The truism above makes plain that there are other considerations ,
such as portability.</p>

<p>The smallest refractors, of 50 to 70 mm (2 to 3 inches) aperture, will show
the Moon’s craters, Saturn’s rings, Jupiter’s cloud belts and its four
main satellites, some attractive double stars, and the brighter nebulae and
galaxies. </p>

<p>The views will not be bright, but with care and in good, dark skies
there’s a lot to see. </p>

<p>However its probably best to start with something with more light gathering
power.</p>

<p></p>

<p><span style="color:#ff0000">Beware!.</span> There are some small refractors
which are not all they appear. They may have a 50 mm lens but at the front end
there is a disc with a hole behind it which cuts the aperture down to around 25
mm. This allows the manufacturer to use a simple lens rather than the more
expensive achromatic variety. The view is very dim, and of very little use.
From the outside, they look impressive, but in practice they are so bad that
most people give up on them straight away. </p>

<p><em>They are often sold in toy shops or on the Internet, and even in some
prestigious department stores, not necessarily very cheaply.</em></p>

<p>Incidentally, many refractors show some false colour unless you go for the
much more expensive variety with what are called ED or apochromatic lenses. But
reflectors and catadioptrics are usually free from it.</p>

<p>Reflecting telescopes are generally made in larger sizes. A typical small
reflector is 100 – 130 mm aperture, (a refractor of this aperture would cost
considerably more). </p>

<p>With catadioptric telescopes you view in the same direction as the object,
as with refractors. They generally cost more than a refractor or reflector of
the same aperture, as you are paying for a complex optical design to make the
instrument more compact.</p>

<p></p>

<p><strong style="color:#800040">Magnification</strong></p>

<p>Again beware of excessive Magnification claims.</p>

<p>My first lesson on reading books by Patrick Moore on telescopes was</p>

<p style="margin-left:2em;"><em>'The maximum useful magnification of a
telescope is twice the aperture in millimetres (or 50 times for each inch of
aperture).</em> '</p>

<p></p>

<p>Those with large telescopes rarely use magnifications much more than about
x250 because our own shimmering atmosphere (‘bad seeing’, when the image is
fuzzy and indistinct). And low magnifications are every bit as useful as high
ones. When finding an object, start wide then increases the power if
circumstances warrant it. Many diffuse objects can only be appreciated at lower
magnification. One of the commonest eyepieces I used on an 80mm 480mm focal
length refractor was a 30mm eyepiece that's just 16x.</p>

<p></p>

<p><strong style="color:#800040">Focal length and the f-number</strong></p>

<p>This bit is important. </p>

<p>The focal length is the distance between the lens and the eyepiece. A
telescope with a focal length of 900 mm would be just under a metre long. </p>

<p>The f-number is the focal length divided by the aperture and tells you
whether the telescope is short and fat or long and thin. A telescope with a
small f-number (such as f/6) is short and fat and gives bright images of low
magnification, while one with a large f-number (such as f/12) is long and thin
and will give dimmer images at higher magnification, assuming that the same
aperture.</p>

<p>Catadioptric telescopes are different, as their optical systems provide a
long focal length and high f-number in a short tube, which makes them more
portable for their size than would otherwise be the case.</p>

<p></p>

<p><strong><span style="color:#800040">Mountings</span></strong></p>

<table max-width="900">
  <tbody>
    <tr>
      <td max-width="580" valign="middle">In many ways this can be more important
        than the scope and way more expensive. To use a telescope one needs a
        sturdy, vibration free mount. If not, one will easily tire of using the
        scope. Camera tripods are invariably unsuitable as vibration makes even
        moderate magnification unusable.<br>
         <br>
        The simplest type of mounting is the altazimuth design, which allows
        one to move the scope up and down and from side to side. <br>
         <br>
        Then there is the equatorial mount, which needs more care and time to
        set up with the polar axis pointing to the north celestial pole, near
        Polaris. An equatorial mount has the advantage that objects can be kept
        within the field of view as the Earth rotates by turning the telescope
        around the poleward-pointing axis only. <br>
         <br>
        Before buying, I believe it is essential to try both types of mount. A
        sturdy altaz mount is often easier to use than a cheap equatorial. <br>
         <br>
        One example is the Dobsonian mount, <em>(right)</em> used for
        reflectors of all sizes. These can allow one to move the telescope just
        by pushing it. There are motorised auto-tracking mounts which, like
        equatorials, do need to be aligned on the sky before they will work, so
        you really need to know the names of the brighter stars at least. And
        then there are Go To mounts, available as both altazimuth and
        equatorials, which will find any object in their databases once they
        are aligned on the sky. Some, such as Celestron’s Sky-Align system,
        don’t require you to know any of the names of the stars or even where
        north is. </td>
