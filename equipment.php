<p></p>

<p><strong style="color:#800040">Which one for you?</strong></p>

<p>In recent years I have seen many recommendations that a first scope should
be a <em><span style="color:#800040">Dobsonian</span></em>. They are cheaper,
don't require an expensive mount, are often more comfortable to use, come in a
variety of apertures and focal lengths and use the same eyepieces that one will
use with the next scope.</p>

<p>I am inclined to agree and would probably start with one if I was beginning
again. </p>

<p>Downsides: moving it into position, collimation (optical alignment of the
mirrors), lack of portability for larger ones. Mounted on a firm trolley on
good quality castors such a scope can be wheeled out of the garage or store and
be ready immediately.</p>

<p>Townies living with light pollution, are better off with a larger f-number
enabling one to push up the magnification and get detailed views of the planets
which are not affected by city lights. It will also give good views of many
deep-sky objects such as nebulae, star clusters and galaxies.</p>

<p>Out in the country, a shorter f-number will give one bright, wide-field
views of nebulae and galaxies, but may struggle to give high magnification
views of planets unless it is of excellent quality. </p>

<p><em style="color:#800040">Reflector, refractor or catadioptric?</em> </p>

<p>A small telescope for occasional astronomy and also for terrestial (needs
erecting prism) use should be a refractor. It’s maintenance-free and
straightforward to use. An altazimuth mount is all one needs. </p>

<p>To see fainter objects, a larger refractor or a reflector is probably
better. </p>

<p>You get more aperture (light) for your money with a reflector.</p>

<p>Go To telescopes sound great but one won't learn much about the sky. </p>

<p>If going for photography, don't until you have some experience. You will
need a good scope and an even better equatorial mount and plenty of cash.</p>

<p></p>

<p><strong><span style="color:#800040">Where to buy</span></strong></p>

<p>Don't buy from a catalogue shop, a supermarket,or high street store who
don't specialise in scopes. They will generally be poor and the quickest way to
dissappointment. </p>

<p>Use a specialist store (see adverts in monthly astronmy magazines). The
specialists all have a presence online. </p>

<p>Join a club and ask for recommendations. Attend a star party and ask for a
look! Most astronomers are only too willing to show off their kit. They are
often proudest of the cheap bit that does a better than expected job.</p>

