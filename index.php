<p><em style="color:#800040"><strong>Amateur astronomy is a fascinating
hobby.</strong></em> </p>

<p>It can be an introduction to several practical and useful technologies.
Instruments used in amateur astronomy range from the unaided eye, binoculars
&amp; standard DSLRs to video cameras, large computer-controlled telescopes and
even radio telescopes.</p>

<p> </p>

<p>For observations, the hard part is finding dark skies that are conveniently
located. Although we can't necessarily provide dark skies and the weather that
goes with, we can help introduce you to this awe inspiring activity! We can
provide access to field trips in Anglesey and the opportunity to observe from
Tegg's Nose.</p>

<p> </p>

<p>One doesn’t always require dark, just a clear sky to observe the Sun. The
Sun can be viewed as white light through low cost simple filters and specialist
Hydrogen Alpha telescopes that are now within the price range of an amateur.</p>

<p> </p>

<p>Joining the Society provides the opportunity to look, learn, record and be
enthralled.</p>

<p>Our members range from novices to those who have spent a lifetime in
astronomical research.</p>

<p>Whether an armchair astronomer, visual observer or skilled astrophotographer
you will find a kindred spirit within the Society.</p>

<p> </p>

<p>Founded in 1990, our aim has always been to make astronomy accessible to
everyone. We meet three times a month for a Workshop, Social evening and
Lecture.</p>
</div>

<div id="colourblock-white2">

<div class="text1">
<p><em style="color:#800040"><strong>Astronomy</strong></em> is often seen as a
daunting pastime in which to become involved. The Society works hard to ensure
that our welcome to existing and new members alike is friendly and informal. We
are simply enthusiastic amateurs who love learn about &amp; look at the Night
Sky.</p>

<p> </p>

<p>In order to become a member of Macclesfield Astronomical Society you need no
formal scientific qualification. However, it is as well to bring enthusiasm and
an enquiring mind along with you!</p>

<p>Astronomy is the one science where an amateur can still make a fundamental
contribution. Though scientific research is not their main goal, many make a
contribution to astronomy by monitoring variable stars, tracking asteroids and
discovering transient objects, such as comets and novae.</p>

<p> </p>

<p>As amateurs we do not depend on the field of astronomy as a primary source
of income or support, and do not have a professional degree or advanced
academic training in the subject. Many amateurs are beginners or hobbyists,
while others have a high degree of experience in astronomy and often assist and
work alongside professional astronomers.</p>

<p>One doesn’t need to invest heavily in equipment to appreciate the heavens,
though if one gets enthusiastic, the budgets required can be extensive. One can
use nothing more than one’s eyes, however, tools for amateur astronomy more
commonly include portable telescopes and binoculars.</p>

<p> </p>

<p>Today, many amateurs are keen Astro Photographers. The quality of images
taken by our members exceeds those taken by professionals only a few years ago.
This ranges from using a compact camera at a telescope eyepiece, a Digital
Single Lens Reflex camera instead of an eyepiece, use of a simple webcam to
specialist Imagers.</p>

<p> </p>

<p>People have studied the sky throughout history in an amateur framework,
without any formal method of funding. It is only within the last century,
however, that amateur astronomy has become an activity clearly distinguished
from professional astronomy, and other related activities.</p>

<p>It has been said that the fundamental difference between the amateur and the
professional, is that the amateur actually knows where the stars are as they
are the astronomers who get out under the night sky.</p>

